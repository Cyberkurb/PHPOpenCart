                        'product_id'  => $result['product_id'],
						'thumb'       => $image,
						'model_num'   => $result['model'],
                        'name'        => $result['name'],
                        'description' => trim(strip_tags(html_entity_decode($result['description'], ENT_QUOTES, 'UTF-8'))),
                        'price'       => $price,
                        'special'     => $special,
                        'exclusive'   => $execlusive_partner,
                        'tax'         => $tax,
                        'minimum'     => $result['minimum'] > 0 ? $result['minimum'] : 1,
						'rating'      => $rating,
						'elite_price'       => $elite_dealer_price,
                        'reserve_price'     => $reserve_dealer_price,
                        'distributer_price' => $distributer_pricing,
                        'distributer_price' => $distributer_pricing,
                        'anoka_inventory'   => $result['quantity_anoka'],
                        'wa_inventory'      => $result['quantity_wa'],
                        'canada_inventory'  => $result['quantity_ca'],
                        'phoenix_inventory' => $result['quantity_phoenix'],
                        'phoenix_inventory' => $result['quantity_phoenix'],
                        'phxdc_inventory'   => $result['quantity_phxdc'],
                        'europe_inventory'  => $result['quantity_europe'],
                        'onorder'           => $result['quantity_onorder'],
                        'onwater'           => $result['quantity_onwater'],
                        'amazen_inventory'  => $result['quantity_amazen'],
                        'fife_inventory'  => $result['quantity_fife'],
                        'expected_availablity' => $result['expected_availablity'],
                        'transit_inventory' => $result['quantity_transit'],
                        'quantity'          => $result['quantity'],
                        'href'        => $this->url->link('product/product', 'product_id=' . $result['product_id']),
                        'cat_id'     => $cat_id,
                        'parent_name' => $parent_name,
                        'cat_name'    => $cat_name
